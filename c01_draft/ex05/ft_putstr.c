#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;
	char	c;

	i = 0;
	while (str[i] != '\0')
	{
		c = str[i];
		write(1, &c, 1);
		i++;
	}
}

int	main(void)
{
	char	frase[] = "Hello Devil World";

	ft_putstr(frase);
	return (0);
}
