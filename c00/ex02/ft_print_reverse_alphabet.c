#include <unistd.h>

void	ft_print_reverse_alphabet(void)
{
	int		x;
	char	c;

	x = 26;
	c = 'z';
	do
	{
	write (1, &c, 1);
	c--;
	x--;
}
while
	(x != 0);

}
